// Задача состоит в том, чтобы написать функцию на языке JavaScript, которая проверяет, является ли заданное неотрицательное целое число степенью двойки. Степенью двойки называется число вида 2^n, где n - целое число, то есть результат возведения в степень с основанием два и целым показателем n.

// Вы можете предположить, что входные данные всегда корректны.

// Примеры
// isPowerOfTwo(1024) // -> true
// isPowerOfTwo(4096) // -> true
// isPowerOfTwo(333)  // -> false

// Один из возможных способов решения задачи - это использовать битовые операции. 

function isPowerOfTwo(n) {
// Проверяем, что n - положительное число
if (n <= 0) {
return false;
}
// Применяем побитовое И к n и n-1
return (n & (n-1)) == 0;
}
