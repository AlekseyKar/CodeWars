// Заданный список a возвращает фиксированный список, чтобы значения увеличивались на 1 для каждого индекса от минимального значения до максимального значения (включая оба). numbers

// Пример
// Input:  1,3,5,6,7,8 Output: 1,2,3,4,5,6,7,8


// Функция, которая принимает список чисел и возвращает список с заполненными пропусками
function fillGaps(numbers) {
// Если список пустой или содержит один элемент, возвращаем его как есть
if (numbers.length <= 1) {
return numbers;
}

// Создаем новый список для результата
let result = [];

// Находим минимальное и максимальное значение в списке
let min = Math.min(...numbers);
let max = Math.max(...numbers);

// Добавляем в результат все числа от минимального до максимального с шагом 1
for (let i = min; i <= max; i++) {
result.push(i);
}

// Возвращаем результат
return result;
}

// Пример использования функции
let input = [1,3,5,6,7,8];
let output = fillGaps(input);
console.log(output); // [1,2,3,4,5,6,7,8]